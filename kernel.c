void descriptor_writestring (uint64_t num){
	int maxPos = 99;
	int lastPos = maxPos;
	char chars[maxPos+1];
	for(int i=1; i<=maxPos; ++i){
		char c = ((num % 10) + '0');
		chars[lastPos] = c;
		num /= 10;
		lastPos--;
	}

	lastPos++;

	//skipping starting zeros
	while(chars[lastPos++] == '0') continue;

	if(lastPos >= maxPos){
		terminal_putchar('0');	
	}

	while (lastPos <= maxPos){
		terminal_putchar(chars[lastPos]);
		lastPos++;
	}

}



void create_descriptor(uint32_t base, uint32_t limit, uint16_t flag){
    uint64_t descriptor;
 
    // Create the high 32 bit segment
    descriptor  =  limit       & 0x000F0000;         // set limit bits 19:16
    descriptor |= (flag <<  8) & 0x00F0FF00;         // set type, p, dpl, s, g, d/b, l and avl fields
    descriptor |= (base >> 16) & 0x000000FF;         // set base bits 23:16
    descriptor |=  base        & 0xFF000000;         // set base bits 31:24
 
    // Shift by 32 to allow for low part of segment
    descriptor <<= 32;
 
    // Create the low 32 bit segment
    descriptor |= base  << 16;                       // set base bits 15:0
    descriptor |= limit  & 0x0000FFFF;               // set limit bits 15:0
 
    //printf("0x%.16llX\n", descriptor);
    //terminal_writestring(descriptor);
    terminal_writestring("0x");
    descriptor_writestring(descriptor);
    terminal_writestring(".16llX\n");
    
}
 
